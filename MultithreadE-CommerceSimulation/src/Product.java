public class Product {
    private int id;
    private String ad;
    private double fiyat;
    private int adet;

    public Product() {
    }

    public Product(int id, String ad, double fiyat, int adet) {
        this.id = id;
        this.ad = ad;
        this.fiyat = fiyat;
        this.adet = adet;
    }
    
    
    

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getAd() {
        return ad;
    }

    public void setAd(String ad) {
        this.ad = ad;
    }

    public double getFiyat() {
        return fiyat;
    }

    public void setFiyat(int fiyat) {
        this.fiyat = fiyat;
    }

    public int getAdet() {
        return adet;
    }

    public void setAdet(int adet) {
        this.adet = adet;
    }

    
}
